-- SQL structured query language
-- used to manipulate store and retrieve and many more functionalities we can do
--  mostly used in rdbms relational database management system
--  RDBMS is a database management system we  can store the data in table
-- table consists of rows and columns
-- rows is called record
-- column is called attributes

-- DDL ---> data definition language ---- CREATE ALTER DROP TRUNCATE RENAME

-- DML ---> data manipulation language --- INSERT UPDATE DELETE 

-- DCL ---> data control language ---- GRANT REVOKE

-- DQL ----> data query language ----- SELECT

-- TCL -----> transaction control language ---- COMMIT ROLLBACK SAVEPOINT 

 -- most commonly used datatypes in sql
 -- VARCHAR 
 -- INT
 -- BOOLEAN
 -- CHAR
 -- FLOAT
 -- DATE
 
 
 -- VARCHAR --- VARIABLES AND CHARACTERS
		    --  allowed alphabets a-z and A-Z 
            --  allowed numbers 0-9
			--  combination of both alphabets and numbers
            -- special characters 
            --  eg: forest for13324 for$%2 
            
 -- INT     -- integer  values are allowed
            -- both positive and negative values
			-- does not store decimal values
-- DATE --   date values in any data format 
	    -- eg: 05-07-1985
		-- eg: 05-july-1986
        -- eg: 1986-07-05
        -- eg; 05/07/1986 
        
-- FLOAT
         --  holds only decimal values
-- BOOLEAN 
         -- stores only true or false